    private void jdepositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jdepositActionPerformed
        long total=0;
        long amount=Long.parseLong(jamount.getText());
        try {
            pstmt=con.prepareStatement("select * from transaction where acc_no=?");
            pstmt.setLong(1,Long.parseLong(jacc_no.getText()));
            rs=pstmt.executeQuery();
            while(rs.next()){
                total=rs.getLong("total");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        try {
            pstmt=con.prepareStatement("insert into transaction values(?,?,?,?)");
            pstmt.setLong(1,Long.parseLong(jacc_no.getText()));
            pstmt.setLong(2, Long.parseLong(jamount.getText()));
            pstmt.setLong(3, 0);
            pstmt.setLong(4, total+amount);
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Amount deposit successfully");
            MainMenu mm= new MainMenu();
            mm.show();
            hide();
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Please enter amount");
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jdepositActionPerformed

