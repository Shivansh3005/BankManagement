    private void jcheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcheckActionPerformed
        hideElements();
        try {
            pstmt=con.prepareStatement("select * from accounts where acc_no=?");
            pstmt.setLong(1, Long.parseLong(jacc_no.getText()));
            rs=pstmt.executeQuery();
            if(rs.next()){
                pstmt=con.prepareStatement("select * from accounts where acc_no=? and pass=?");
                pstmt.setLong(1, Long.parseLong(jacc_no.getText()));
                pstmt.setString(2, jpass.getText());
                rs=pstmt.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "Authentication sucessful");
                    showElements();
                    jacc_no.setEnabled(false);
                    jpass.setEnabled(false);
                    jcheck.setEnabled(false);
                }else{
                    JOptionPane.showMessageDialog(null, "Wrong Password");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Account not found");
            }
        } catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Please enter account number");
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jcheckActionPerformed

