    private void jenterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jenterActionPerformed
        DateFormat df= new SimpleDateFormat("yyyy-MM-dd");
        
        String acc_no=jacc_no.getText();
        try {
            pstmt=con.prepareStatement("select * from accounts where acc_no=?");
            pstmt.setLong(1, Long.parseLong(acc_no));
            rs=pstmt.executeQuery();

            if(rs.next()){//        here when completing project have to add a condition of date of birth
                if((rs.getLong("m_no")==Long.parseLong(jmno.getText()))
                        &&(rs.getString("dob").equals(df.format(jdob.getDate())))
                        &&(rs.getLong("aadhar")==Long.parseLong(jaadhar.getText()))){
                    NewPassword np= new NewPassword(acc_no);
                    np.show();
                    hide();
                    try {
                        con.close();
                        pstmt.close();
                        rs.close();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                    }
                }else{
                    JOptionPane.showMessageDialog(null,"Wrong input");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Acount not found");
            }

        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Input data");
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jenterActionPerformed

    private void jcheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcheckActionPerformed
        String acc_no=(jacc_no.getText());
        try {
            pstmt=con.prepareStatement("select * from accounts where acc_no=?");
            pstmt.setLong(1, Long.parseLong(acc_no));
            rs=pstmt.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "account found");
                showElements();
            }else{
                JOptionPane.showMessageDialog(null, "Account not found");
                hideElements();
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Account number can't be empty");
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jcheckActionPerformed

