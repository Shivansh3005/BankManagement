    private void jacc_noKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jacc_noKeyPressed
        String account=jacc_no.getText();
        int len=account.length();
        char c= evt.getKeyChar();
        if(evt.getKeyChar()>='0' && evt.getKeyChar()<='9'){
            if(len<10){
                jacc_no.setEditable(true);
            }else{
                jacc_no.setEditable(false);
            }
        }else{
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode()==KeyEvent.VK_DELETE){
                jacc_no.setEditable(true);
            }else{
                jacc_no.setEditable(false);
            }
        }
    }//GEN-LAST:event_jacc_noKeyPressed

