    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel dtm= (DefaultTableModel)jtable.getModel();//default table model to put data into table
        dtm.setRowCount(0);//set 0 rows in jtable
        try {
            Long acc_no=Long.valueOf(jacc_no.getText());
            String pass= jpass.getText();
            pstmt=con.prepareStatement("select * from accounts where acc_no=?");
            pstmt.setLong(1, acc_no);
            rs=pstmt.executeQuery();
            if(rs.next()){
                jforgot.setEnabled(true);//it will only show when account number is correct
                pstmt=con.prepareStatement("select * from accounts where acc_no= ? and pass= ?");
                pstmt.setLong(1, acc_no);
                pstmt.setString(2, pass);
                rs=pstmt.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "Authentication successful");
                    jforgot.setEnabled(false);//to hide forgot button because it should not be shown when password is correct
                    String account= Long.toString(rs.getLong("acc_no"));
                    String name=rs.getString("name");
                    String dob= rs.getString("dob");
                    String gender=rs.getString("gender");
                    String mno=rs.getString("m_no");
                    String doc=rs.getString("doc");
                    String query= "select * from transaction where acc_no=?";
                    pstmt=con.prepareStatement(query);
                    pstmt.setLong(1,acc_no);
                    rs=pstmt.executeQuery();
                    String balance=null;
                    while(rs.next()){
                        balance= Long.toString(rs.getLong("total"));//to get data from table transaction
                    }
                    String[] data={account,name,dob,gender,mno,doc,balance};//to put data into jtable                   
                    dtm.addRow(data);
                }else{
                    JOptionPane.showMessageDialog(null, "Wrong password");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Wrong account number");
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Account number can't be empty");
        }catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

