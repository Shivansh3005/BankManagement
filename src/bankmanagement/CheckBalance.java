    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            pstmt=con.prepareStatement("select * from accounts where acc_no=?");
            pstmt.setLong(1, Long.parseLong(jacc_no.getText()));
            rs=pstmt.executeQuery();
            if(rs.next()){
                pstmt=con.prepareStatement("select * from accounts where acc_no=? and pass=?");
                pstmt.setLong(1, Long.parseLong(jacc_no.getText()));
                pstmt.setString(2, jpass.getText());
                rs=pstmt.executeQuery();
                if(rs.next()){
                   JOptionPane.showMessageDialog(null, "Your balance is "+Balance(Long.valueOf(jacc_no.getText())));
                }else{
                    JOptionPane.showMessageDialog(null, "Wrong Password");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Account not found");
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Please enter account number");
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    public long Balance(Long account){
        long balance=0;
        try {
            pstmt=con.prepareStatement("select * from transaction where acc_no=?");
            pstmt.setLong(1,account);
            rs=pstmt.executeQuery();
            while(rs.next()){
                balance=rs.getLong("total");
            }
        } catch (Exception e) {
        }
        return balance;
    }
    
    private void jacc_noKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jacc_noKeyPressed
        String account=jacc_no.getText();
        int len=account.length();
        char c= evt.getKeyChar();
        if(evt.getKeyChar()>='0' && evt.getKeyChar()<='9'){
            if(len<10){
                jacc_no.setEditable(true);
            }else{
                jacc_no.setEditable(false);
            }
        }else{
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode()==KeyEvent.VK_DELETE){
                jacc_no.setEditable(true);
            }else{
                jacc_no.setEditable(false);
            }
        }
    }//GEN-LAST:event_jacc_noKeyPressed

