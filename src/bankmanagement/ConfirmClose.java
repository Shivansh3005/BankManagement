    private void jcloseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcloseActionPerformed
        if((jreason.getSelectedItem().equals("Select"))&&(jpass.getText().equals(""))&&(!jyes.isSelected())){
           JOptionPane.showMessageDialog(null, "All fields required");
        }else if(jreason.getSelectedItem().equals("Select")){
           JOptionPane.showMessageDialog(null, "Please select a reason");
        }else if(jpass.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Please enter password");     
        }else if(!jyes.isSelected()){
           JOptionPane.showMessageDialog(null, "Please check Yes");    
        }else{
            
            try {
                pstmt=con.prepareStatement("select * from accounts where acc_no=? and pass=?");
                pstmt.setLong(1, acc_no);
                pstmt.setString(2, jpass.getText());
                rs=pstmt.executeQuery();
                if(rs.next()){
                    pstmt=con.prepareStatement("delete from accounts where acc_no=?");
                    pstmt.setLong(1, acc_no);
                    pstmt.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Account closed sucessfully");
                    MainMenu mm= new MainMenu();
                    mm.show();
                    hide();
                }else{
                    JOptionPane.showMessageDialog(null, "Wrong password");
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jcloseActionPerformed

