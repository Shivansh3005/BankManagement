    public final void jdbcConnection(){
        String url="jdbc:mysql://localhost:3306/bank";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection(url,"root","shiv");
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void jconfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jconfirmActionPerformed
        
        
        if((jpass.getText().equals(""))||(jcpass.getText().equals(""))){
            JOptionPane.showMessageDialog(null, "Please enter new password");
        }
        else if(!(jpass.getText().equals(jcpass.getText()))){
            JOptionPane.showMessageDialog(null, "Confirm password is not matched");
        }
        else{
            try {
                
                pstmt=con.prepareStatement("update accounts set pass=? where acc_no=?");
                pstmt.setString(1, jpass.getText());
                pstmt.setString(2, this.acc_no);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Password changed sucessfully");
                MainMenu mm= new MainMenu();
                mm.show();
                hide();
                
            } catch (SQLException ex) {
                Logger.getLogger(NewPassword.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
    }//GEN-LAST:event_jconfirmActionPerformed

